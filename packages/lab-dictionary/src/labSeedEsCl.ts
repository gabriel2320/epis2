import type { LabDictionaryEntry, LabPanelId } from './types.js';

type SeedInput = {
  id: string;
  label: string;
  synonyms?: readonly string[];
  unit: string;
  referenceLow?: number;
  referenceHigh?: number;
  criticalLow?: number;
  criticalHigh?: number;
  panel: LabPanelId;
  loinc?: string;
};

function seed(input: SeedInput): LabDictionaryEntry {
  return {
    id: input.id,
    label: input.label,
    synonyms: input.synonyms ?? [],
    unit: input.unit,
    panel: input.panel,
    ...(input.referenceLow !== undefined ? { referenceLow: input.referenceLow } : {}),
    ...(input.referenceHigh !== undefined ? { referenceHigh: input.referenceHigh } : {}),
    ...(input.criticalLow !== undefined ? { criticalLow: input.criticalLow } : {}),
    ...(input.criticalHigh !== undefined ? { criticalHigh: input.criticalHigh } : {}),
    ...(input.loinc ? { loinc: input.loinc } : {}),
  };
}

