import type { DrugDictionaryEntry, DrugLocalUse, DrugRouteEsCl } from './types.js';

type SeedInput = {
  id: string;
  activeIngredient: string;
  commonNames?: readonly string[];
  usualOrders: readonly string[];
  routes: readonly DrugRouteEsCl[];
  warnings?: readonly string[];
  renalAdjustment?: string;
  localUse?: readonly DrugLocalUse[];
};

function seed(input: SeedInput): DrugDictionaryEntry {
  return {
    id: input.id,
    activeIngredient: input.activeIngredient,
    commonNames: input.commonNames ?? [input.activeIngredient],
    usualOrders: input.usualOrders,
    routes: input.routes,
    warnings: input.warnings ?? [],
    localUse: input.localUse ?? ['hospitalizado', 'ambulatorio'],
    ...(input.renalAdjustment ? { renalAdjustment: input.renalAdjustment } : {}),
  };
}

